#include "libft.h"

void	ft_putnbr_fd(int n, int fd)
{
	if (n < 0)
	{
		ft_putchar_fd('-', fd);
		if (n == -2147483648)
		{
			ft_putnbr_fd((-(n + 1)) / 10, fd);
			ft_putnbr_fd(8, fd);
			n = -1;
		}
		else
			n = -n;
	}
	if (n <= 9 && n >= 0)
		ft_putchar_fd(n + '0', fd);
	if (n >= 10)
	{
		ft_putnbr_fd(n / 10, fd);
		ft_putnbr_fd(n % 10, fd);
	}
}
