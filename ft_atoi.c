#include "libft.h"

int	ft_atoi(const char *str)
{
	int			sign;
	long long	num;

	sign = 1;
	while (*str == ' ' || *str == '\n' || *str == '\v' || *str == '\f'
			|| *str == '\r' || *str == '\t')
		str++;
	if (*str == '-' || *str == '+')
	{
		if (*str == '-')
			sign = -1;
		str++;
	}
	num = 0;
	while (*str >= '0' && *str <= '9')
	{
		num = num * 10;
		num = num + (*str - '0');
		str++;
	}
	return (num * sign);
}
